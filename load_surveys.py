def make_filename(filename: str) -> str:
    filename = filename.replace(".xlsx", "")
    start, end = [d.split('.') for d in filename.split()[1].split('-')]

    year = ""
    month = ""
    day_s = start[0]
    day_e = end[0]

    if len(start) == 2:
        month = start[1]
    else:
        month = end[1]

    if len(end[2]) == 2:
        year = '20' + end[2]
    else:
        year = end[2]

    return f'auto_{year}.{month}.{day_s}-{day_e}_AFFB.xlsx'


